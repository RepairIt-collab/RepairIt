const Dico={ ...Electricite, ...Mecanique };

// TROUVER LES SUGGESTIONS DANS LE DICTIONNAIRE  
  const searchInput = document.getElementById('recherche');
  const suggestionsList = document.getElementById('suggestionsList');

  searchInput.addEventListener('input', function () {
    const keyword = searchInput.value.toLowerCase();
    const matchingKeys = [];
  
    for (const key in Dico) {
      const lowercaseKey = key.toLowerCase();
      if (lowercaseKey.startsWith(keyword)) {
        matchingKeys.push(key);
      }
    }
  
    Afficher_Suggestion(matchingKeys);
  });
  
  // LES AFFICHER 
  function Afficher_Suggestion(suggestions) {
    suggestionsList.innerHTML = '';
  
    for (const suggestion of suggestions) {
      const listItem = document.createElement('li');
      listItem.textContent = suggestion;
      listItem.addEventListener('click', function() {
        const value = this.textContent;
        document.getElementById('recherche').value=value;
      });
      suggestionsList.appendChild(listItem);
    }
  }

//   AFFICHER LE DOMAINE CORRESPONDANT
